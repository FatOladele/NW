def displayboard(board):
  boardtext = ""
  #print(board)
  for row in board:
    for column in row:
      if column == 0:
        boardtext = boardtext + " "
      elif column > 110 and column < 200:
        x = column - 111
        boardtext = boardtext + alphabetlist[x]
      elif column > 200:
        x = column - 211
        boardtext = boardtext + alphabetlist[x].upper()
      boardtext = boardtext + "|"
    boardtext = boardtext + "\n_______________________________\n"
    #print(boardtext)
  print(boardtext)

def newboard(board, tiles):
  for bcode in list(tiles.keys()):
    py = ((letcode[bcode[0]]) - 1)
    px = (int(bcode[1:]) -1)
    board[py][px] = tiles[bcode]
  return board
def slove(board ,rack):
  starttime = time.time()
  displayboard(board)
  sloved = ai.computerplay(board, rack, 5, 'English')
  print(sloved)
  slovedboard = newboard(board, sloved[2])
  displayboard(slovedboard)
  usedtime = time.time() - starttime
  print(usedtime)
